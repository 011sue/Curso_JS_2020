// ATRIBUIÇÃO =

const a = 7
let b = 3
b += a 
// adição e atribuição
// acrescentando valor de A a var B, ou seja 10
console.log('adição', b)

b -= 4
// subtração e atribuição
// b = b - 4
console.log('subtração -', b)

b *= 4 
// multiplicação e atribuição
// b = b * 4
console.log('multiplicação -', b)

b /= 4
// divisão e atribuição
console.log('divisão -', b)

b %= 4
// modular e atribuição - restante da div
console.log('restante div -', b)
